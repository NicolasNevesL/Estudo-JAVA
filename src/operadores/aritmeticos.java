package operadores;

public class aritmeticos {
    public static void main(String[] args) {
        // + - / *
        int numero01 = 10;
        int numero02 = 20;
        int resultadoSoma = numero02 + numero01;
        System.out.println(resultadoSoma);

        int numero03 = 10;
        int numero04 = 20;
        int resultadoMultiplicacao = numero03 * numero04;
        System.out.println(resultadoMultiplicacao);

        int numero05 = 10;
        int numero06 = 20;
        int resultadoSubtracao = numero06 - numero05;
        System.out.println(resultadoSubtracao);

        int numero07 = 10;
        double numero08 = 20;
        double resultadoDivisao = numero07 / numero08;
        System.out.println(resultadoDivisao);
    }
}
